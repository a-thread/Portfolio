$(document).ready(function () {

    $('ul.navbar-nav li.dropdown').hover(function () {
        $(this).find('.dropdown-menu').stop(true, true).delay(200).fadeIn(500);
    }, function () {
        $(this).find('.dropdown-menu').stop(true, true).delay(200).fadeOut(500);
    });

    ScrollReveal().reveal('button.btn-outline-light', { delay: 1000 });
    ScrollReveal().reveal('.typewrite', { delay: 650 });

    ScrollReveal().reveal("div.bio-container", {
        viewFactor: 0.2,
        reset: true
    });

    ScrollReveal().reveal("#portfolio", {
        viewFactor: 0.02,
        reset: true
    });

    ScrollReveal().reveal("#testimonials", {
        viewFactor: 0.2,
        reset: true
    });

    ScrollReveal().reveal("#contact", {
        viewFactor: 0.2,
        reset: true
    });

    // intro typing effects (https://css-tricks.com/snippets/css/typewriter-effect/)
    var TxtType = function (el, toRotate, period) {
        this.toRotate = toRotate;
        this.el = el;
        this.loopNum = 0;
        this.period = parseInt(period, 10) || 2000;
        this.txt = '';
        this.tick();
        this.isDeleting = false;
    };

    TxtType.prototype.tick = function () {
        var i = this.loopNum % this.toRotate.length;
        var fullTxt = this.toRotate[i];

        if (this.isDeleting) {
            this.txt = fullTxt.substring(0, this.txt.length - 1);
        } else {
            this.txt = fullTxt.substring(0, this.txt.length + 1);
        }

        this.el.innerHTML = '<span class="wrap">' + this.txt + '</span>';

        var that = this;
        var delta = 200 - Math.random() * 100;

        if (this.isDeleting) { delta /= 2; }

        if (!this.isDeleting && this.txt === fullTxt) {
            delta = this.period;
            this.isDeleting = true;
        } else if (this.isDeleting && this.txt === '') {
            this.isDeleting = false;
            this.loopNum++;
            delta = 500;
        }


        setTimeout(function () {
            that.tick();
        }, delta);
    };

    setTimeout(function () {
        var elements = document.getElementsByClassName('typewrite');
        for (var i = 0; i < elements.length; i++) {
            var toRotate = elements[i].getAttribute('data-type');
            var period = elements[i].getAttribute('data-period');
            if (toRotate) {
                new TxtType(elements[i], JSON.parse(toRotate), period);
            }
        }
        // INJECT CSS
        var css = document.createElement("style");
        css.type = "text/css";
        css.innerHTML = ".typewrite > .wrap { border-right: hidden}";
        document.body.appendChild(css);
    }, 800);
});